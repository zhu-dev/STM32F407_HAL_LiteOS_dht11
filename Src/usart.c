///重定向c库函数printf到USART1
int fputc(int ch, FILE *f)
{ 	
	while((USART1->SR&0X40)==0);//循环发送,直到发送完毕   
	USART1->DR = (uint8_t) ch;      
	return ch;
}
///重定向c库函数scanf到USART1
int fgetc(FILE *f)
{
    /* 等待串口1输入数据 */
    while((USART1->SR&UART_FLAG_RXNE)==0);
    return (int)USART1->DR&0xff;
}


 void  OS_Debug_Printf(char *format, ...)
{
	
			UINT32 uwRet = LOS_OK;
	
     char  buf_str[128];
     va_list   v_args;
  
  
      va_start(v_args, format);
     (void)vsnprintf((char       *)&buf_str[0],
                     (size_t      ) sizeof(buf_str),
                    (char const *) format,
                                   v_args);
     va_end(v_args);
 
     /* 互斥锁保护串口资源，保证数据包完整输出 */
     uwRet = LOS_MuxPend(MUTEX_OS_Printf_Handle , LOS_WAIT_FOREVER);  //获取互斥锁
		 if(uwRet == LOS_OK){
			 printf("%s", buf_str);
			}	
     LOS_MuxPost( MUTEX_OS_Printf_Handle ); //给出互斥锁
 }

